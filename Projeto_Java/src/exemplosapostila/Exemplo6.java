package exemplosapostila;

public class Exemplo6 {
   public static void main(String[] args) {
       int x = 0;
        x++; // x = x + 1
        System.out.println(x); //1

        ++x;
        System.out.println(x); //2

        System.out.println(x++); //2

        System.out.println(x); // vai valer 3

        System.out.println(++x); //4


    }
}
